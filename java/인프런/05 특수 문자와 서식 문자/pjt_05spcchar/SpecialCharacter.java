package pjt_05spcchar;

public class SpecialCharacter {
	public static void main(String[] args) {

		// 행 주석

		/*
		 * 문서 주석
		 */

		/* 블록 주석 */

		System.out.println("Good\tMorning~"); // \t (탭, 스페이스 4칸)
		System.out.println("Good\nMorning~"); // \n (줄바꿈)
		System.out.println("Good\'Morning~\'"); // \' ('작은 따옴표)
		System.out.println("Good\"Morning~\""); // \" ("큰 따옴표)
		System.out.println("Good\\Morning~\\"); // \\ (\역슬래시)
	}
}
